  function getFileName() {
    var path = window.location.pathname || '';
    var parts = path.split('/').filter(Boolean);
    var last = (parts[parts.length - 1] || '').toLowerCase();

    if (!last) {
      return 'index.html';
    }

    if (last.indexOf('.') === -1) {
      return last + '.html';
    }

    return last;
  }

  function setText(selector, value) {
    var node = document.querySelector(selector);
    if (node && typeof value === 'string') {
      node.textContent = value;
    }
  }

  function applyCommon(copy, lang) {
    document.documentElement.lang = lang;
    document.title = copy.title;

    var navLinks = document.querySelectorAll('.left-actions .top-btn');
    navLinks.forEach(function (link) {
      var href = (link.getAttribute('href') || '').toLowerCase();
      if (href === 'index.html') {
        link.textContent = copy.navHome;
      }
      if (href === 'care.html') {
        link.textContent = copy.navCare;
      }
      if (href === 'about.html') {
        link.textContent = copy.navAbout;
      }
    });

    setText('#mobile-about', copy.navAbout || copy.navHome || 'About');
    setText('#mobile-care', copy.navCare || 'Care');
    setText('#mobile-contact', lang === 'mn' ? 'Холбоо барих' : 'Contact');

    var topMetaLinks = document.querySelectorAll('.top-right-meta .meta-item[href]');
    topMetaLinks.forEach(function (link) {
      var href = (link.getAttribute('href') || '').toLowerCase();
      if (href === 'index.html') {
        link.textContent = copy.backToSite;
      }
    });

    var label = document.getElementById('page-lang-label-static');
    if (label) {
      label.textContent = lang === 'mn' ? 'EN' : 'MN';
    }

    var toggle = document.getElementById('page-lang-toggle-static');
    if (toggle) {
      toggle.setAttribute('aria-label', lang === 'mn' ? 'Switch to English' : 'Монгол хэл рүү шилжүүлэх');
    }
  }

  function applyCarePage(copy) {
    setText('.care-kicker', copy.kicker);
    setText('#care-guide-title', copy.h1);
    setText('.care-guide-head p:last-child', copy.intro);

    var blocks = document.querySelectorAll('.care-block');
    if (blocks.length < 4) {
      return;
    }

    // Block 1
    setText('.care-block:nth-of-type(1) h3', copy.blocks[0].h3);
    setText('.care-block:nth-of-type(1) .care-block-content > p', copy.blocks[0].p);
    var contactItems = document.querySelectorAll('.care-block:nth-of-type(1) .care-contact-list li');
    contactItems.forEach(function (li, i) {
      if (copy.blocks[0].list[i]) {
        li.textContent = copy.blocks[0].list[i];
      }
    });

    // Block 2
    setText('.care-block:nth-of-type(2) h3', copy.blocks[1].h3);
    setText('.care-block:nth-of-type(2) .care-block-content > p', copy.blocks[1].p);
    var steps = document.querySelectorAll('.care-block:nth-of-type(2) .care-steps li');
    steps.forEach(function (li, i) {
      var stepCopy = copy.blocks[1].steps[i];
      if (!stepCopy) {
        return;
      }
      li.innerHTML = '<strong>' + stepCopy.strong + '</strong> ' + stepCopy.text;
    });

    // Block 3
    setText('.care-block:nth-of-type(3) h3', copy.blocks[2].h3);
    setText('.care-block:nth-of-type(3) > p', copy.blocks[2].p);
    var deepList = document.querySelectorAll('.care-block:nth-of-type(3) .care-bullets li');
    deepList.forEach(function (li, i) {
      if (copy.blocks[2].list[i]) {
        li.textContent = copy.blocks[2].list[i];
      }
    });

    // Block 4
    setText('.care-block:nth-of-type(4) h3', copy.blocks[3].h3);
    var caution = document.querySelectorAll('.care-block:nth-of-type(4) .care-caution-list li');
    caution.forEach(function (li, i) {
      if (copy.blocks[3].list[i]) {
        li.textContent = copy.blocks[3].list[i];
      }
    });
  }

  function renderSection(sectionNode, sectionCopy) {
    if (!sectionNode || !sectionCopy) {
      return;
    }
    var h2 = sectionNode.querySelector('h2');
    if (h2) {
      h2.textContent = sectionCopy.h2;
    }

    var p = sectionNode.querySelector(':scope > p');
    if (p) {
      p.textContent = sectionCopy.p || '';
    }

    var list = sectionNode.querySelector(':scope > ul');
    if (!list) {
      return;
    }

    if (Array.isArray(sectionCopy.items) && sectionCopy.items.length) {
      list.innerHTML = sectionCopy.items.map(function (text) {
        return '<li>' + text + '</li>';
      }).join('');
    }
  }

  function fillList(selector, items) {
    var list = document.querySelector(selector);
    if (!list || !Array.isArray(items)) {
      return;
    }
    list.innerHTML = items.map(function (text) {
      return '<li>' + text + '</li>';
    }).join('');
  }

